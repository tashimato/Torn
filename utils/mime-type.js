function extToMime (extention) {
  if (typeof extention !== 'string') { throw new Error('file name must be a string') }
  return exts[extention.toLowerCase()]
}

function mimeToExt (mime) {
  if (typeof mime !== 'string') { throw new Error('file name must be a string') }
  return mimes[mime.toLowerCase()]
}

